      <blockquote class="blockquote">
        <p class="mb-0">
        Neocosur fue creado en 1997 tras una convocatoria de José Luis Tapia para formar un grupo colaborativo de trabajo, como propuesta ante la necesidad de tener datos localesy la misión de contribuir al mejoramiento continuo de los indicadores de salud neonatal de la región. Con gran entusiasmo se integraron 11 Unidades Neonatales de Argentina, Chile y Uruguay.</p>
